#include "libft.h"

int	ft_memcmp(const void *str1, const void *str2, size_t num)
{
	size_t	i;

	i = 0;
	while (i < num)
	{
		if (((unsigned char *)str1)[i] != ((unsigned char *)str2)[i])
			return (((unsigned char *)str1)[i] - ((unsigned char *)str2)[i]);
		else
			i++;
	}
	return (0);
}
